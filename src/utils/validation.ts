export function isPrecompileAddress(address: string): boolean {
  const lowerAddress = address.toLowerCase();

  // Precompile addresses are 0x0000...0001 through 0x0000...0011
  const precompileAddresses = [
    "0x0000000000000000000000000000000000000001", // ecrecover
    "0x0000000000000000000000000000000000000002", // SHA2-256
    "0x0000000000000000000000000000000000000003", // RIPEMD-160
    "0x0000000000000000000000000000000000000004", // identity
    "0x0000000000000000000000000000000000000005", // modexp
    "0x0000000000000000000000000000000000000006", // bn256Add
    "0x0000000000000000000000000000000000000007", // bn256ScalarMul
    "0x0000000000000000000000000000000000000008", // bn256Pairing
    "0x0000000000000000000000000000000000000009", // blake2f
    "0x000000000000000000000000000000000000000a", // bls12381g1add
    "0x000000000000000000000000000000000000000b", // bls12381g1mul
    "0x000000000000000000000000000000000000000c", // bls12381g1multiexp
    "0x000000000000000000000000000000000000000d", // bls12381g2add
    "0x000000000000000000000000000000000000000e", // bls12381g2mul
    "0x000000000000000000000000000000000000000f", // bls12381g2multiexp
    "0x0000000000000000000000000000000000000010", // bls12381pairing
    "0x0000000000000000000000000000000000000011", // bls12381msm
  ];

  return precompileAddresses.includes(lowerAddress);
}
